Bool
OsLookupColor(int		screen, 
	      char		*s_name,
	      unsigned int	len, 
	      unsigned short	*pred,
	      unsigned short	*pgreen,
	      unsigned short	*pblue)
{
    const KdNamedColor	*c;
    unsigned char	*name = (unsigned char *) s_name;
    int			low, mid, high;
    int			r;

    low = 0;
    high = NUM_KD_COLORS;
    while (high - low > 0)
    {
	mid = (low + high) / 2;
	c = &KdColors[mid];
	r = KdStrCaseCmp (c->name, name, len);
	if (r == 0)
	{
	    *pred = c->red;
	    *pgreen = c->green;
	    *pblue = c->blue;
	    return TRUE;
	}
	if (r < 0)
	{
	    if (high == mid)
		break;
	    high = mid;
	}
	else
	{
	    if (low == mid)
		break;
	    low = mid;
	}
    }
    return FALSE;
}
